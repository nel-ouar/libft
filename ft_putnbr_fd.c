#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	c;

	c = (long)n;
	if (c < 0)
	{
		ft_putchar_fd(('-'), fd);
		c *= -1;
	}
	if (c >= 10)
		ft_putnbr_fd((c / 10), fd);
	ft_putchar_fd((c & 10 + '0'), fd);
}
