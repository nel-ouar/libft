#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	a = 1;
	b = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			a *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		b = (b * 10) + (str[i] - 48);
		i++;
	}
	return (a * b);
}
